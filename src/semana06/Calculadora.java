package semana06;

public class Calculadora {
     // método que soma dois numeros e retorna o resultado
     public static double somar (double n1, double n2) {
     return n1 + n2;
    }

    // método que subtrai dois números e retorna o resultado
    public static double subtrair (double n1, double n2) {
        return n1 - n2;
    }

    // método que multiplica dois numeros e retorna o resultado
    public static double multiplicar (double n1, double n2) {
        return n1 * n2;
    }

    // método que divide dois numeros e retorna o resultado
    public static double dividir (double n1, double n2) {
        return n1 / n2;
    }
}